package pl.javastart.Przeciazanie_Ex1;

public class Calculator {
    static public int add(int a, int b) {
        return a + b;
    }

    static public int add(int a, int b, int c) {
        return a + b + c;
    }

    static public int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }
}
